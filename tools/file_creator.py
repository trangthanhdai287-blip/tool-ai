#!/usr/bin/env python3
"""
tools/file_creator.py

A small CLI to create files safely from templates or direct content. Intended to be called by an AI assistant or by a developer to create many file types on demand.

Usage examples:

# Create a single Python file from a built-in template
python tools/file_creator.py --path tools/port_scanner.py --template port_scanner

# Create multiple files described in a JSON file
python tools/file_creator.py --json requests/create_files.json

# Create a file with literal content
python tools/file_creator.py --path README_NEW.md --content "# New README\nGenerated by AI"

"""

import argparse
import json
import os
import sys
from pathlib import Path
from typing import Dict, Any

