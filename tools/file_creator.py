def sanitize_path(p: str) -> Path:
    """Sanitize and return a safe Path object.

    - Disallow absolute paths
    - Disallow path traversal (..)
    - Resolve relative path to project root (cwd)
    """
    if os.path.isabs(p):
        raise ValueError('Absolute paths are not allowed')
    if '..' in Path(p).parts:
        raise ValueError('Path traversal (..) is not allowed')
    return Path(p)


def ensure_parent(path: Path) -> None:
    parent = path.parent
    if not parent.exists():
        parent.mkdir(parents=True, exist_ok=True)


def create_file(path: Path, content: str, force: bool = False) -> None:
    if path.exists() and not force:
        raise FileExistsError(f'File {path} already exists (use --force to overwrite)')
    ensure_parent(path)
    with open(path, 'w', encoding='utf-8') as fh:
        fh.write(content)


def handle_single(args: argparse.Namespace) -> None:
    path = sanitize_path(args.path)
    if args.template:
        template_name = args.template
        if template_name not in TEMPLATES:
            raise KeyError(f'Unknown template: {template_name}')
        content = TEMPLATES[template_name]
    elif args.content:
        content = args.content
    elif args.content_file:
        with open(args.content_file, 'r', encoding='utf-8') as fh:
            content = fh.read()
    else:
        raise ValueError('One of --template, --content, or --content-file must be provided')
    create_file(path, content, force=args.force)
    print(f'Created {path}')


def handle_json(args: argparse.Namespace) -> None:
    with open(args.json, 'r', encoding='utf-8') as fh:
        data = json.load(fh)
    if not isinstance(data, list):
        raise ValueError('JSON file must contain a list of file descriptors')
    created = []
    for item in data:
        # each item: {"path": "foo.py", "template": "port_scanner"} or {"path": "x","content":"..."}
        if 'path' not in item:
            print('Skipping entry without path:', item)
            continue
        path = sanitize_path(item['path'])
        if 'template' in item:
            tpl = item['template']
            if tpl not in TEMPLATES:
                print('Unknown template', tpl, 'skipping')
                continue
            content = TEMPLATES[tpl]
        elif 'content' in item:
            content = item['content']
        elif 'content_file' in item:
            with open(item['content_file'], 'r', encoding='utf-8') as fh:
                content = fh.read()
        else:
            print('No content for', item['path'], 'skipping')
            continue
        try:
            create_file(path, content, force=args.force)
            created.append(str(path))
        except Exception as e:
            print('Failed to create', path, ':', e)
    print('Created files:', created)


def list_templates() -> None:
    print('Available templates:')
    for t in sorted(TEMPLATES.keys()):
        print(' -', t)


def main(argv: Any = None) -> int:
    ap = argparse.ArgumentParser(description='Create files from templates or content (safe).')
    group = ap.add_mutually_exclusive_group(required=False)
    group.add_argument('--path', help='Relative path to create')
    group.add_argument('--json', help='JSON file describing multiple files to create')
    ap.add_argument('--template', help='Use a built-in template name')
    ap.add_argument('--content', help='Literal content for the file')
    ap.add_argument('--content-file', help='Read content from this file')
    ap.add_argument('--force', action='store_true', help='Overwrite existing files')
    ap.add_argument('--list-templates', action='store_true', help='List available templates')
    args = ap.parse_args(argv)

    try:
        if args.list_templates:
            list_templates()
            return 0
        if args.json:
            handle_json(args)
            return 0
        if args.path:
            handle_single(args)
            return 0
        ap.print_help()
        return 1
    except Exception as e:
        print('Error:', e, file=sys.stderr)
        return 2


if __name__ == '__main__':
    raise SystemExit(main())
